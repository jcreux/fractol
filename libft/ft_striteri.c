#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int	i;

	i = 0;
	if (s)
		while (s[i] && s != NULL && f != NULL)
		{
			(*f)(i, &s[i]);
			i++;
		}
}
