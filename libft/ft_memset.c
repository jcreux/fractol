#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*ptr_s;

	i = 0;
	ptr_s = s;
	while (i < n)
	{
		ptr_s[i] = c;
		i++;
	}
	return (s);
}
